package com.task0.DecisionMakingAndLoops;

public class CheckingAlphabet {
    public static void main(String[] args) {
        char c = '3';

        if(c>='a' && c<='z'){
            System.out.println(c+" is an alphabet");
        }
        else{
            System.out.println(c+" is not an alphabet");
        }
    }
}
